// This code demonstrates the use of for...of loops in JavaScript
// to iterate over strings and arrays

// Example 1: Iterating over a string
let stri = "My name is Soham Deshmukh";

// Use a for...of loop to iterate over each character in the string
for (let character of stri) {
  // Log each character to the console
  console.log(character);
}

// Example 2: Iterating over an array
let b = [1, 2, 3, 4, 5];

// Use a for...of loop to iterate over each element in the array
for (let element of b) {
  // Log each element to the console
  console.log(element);
}